# Настройка логирования
logging.basicConfig(level=logging.INFO)

# Создаем объекты бота и диспетчера
bot = Bot(token=TOKEN)
dp = Dispatcher()

# Переменная для отслеживания состояния карусели
carousel_active = False

# Создаем клавиатуру
keyboard = ReplyKeyboardMarkup(
    keyboard=[
        [KeyboardButton(text="Карусель комплиментов 🎉")],
        [KeyboardButton(text="Достаточно, я довольна 😌")],
        [KeyboardButton(text="Внеплановый комплимент 💖")]
    ],
    resize_keyboard=True
)

# Обработчик команды /start
@dp.message(Command("start"))
async def start(message: types.Message):
    await message.answer("Привет! Ты можешь получить комплимент в любой момент.", reply_markup=keyboard)

# Обработчик кнопки "Карусель комплиментов 🎉"
@dp.message(lambda message: message.text == "Карусель комплиментов 🎉")
async def start_carousel(message: types.Message):
    global carousel_active
    carousel_active = True
    await message.answer("Карусель комплиментов началась! Каждый 15 секунд получаешь новый комплимент. 😊", reply_markup=keyboard)
    while carousel_active:
        compliment = random.choice(compliments)
        await bot.send_message(LENA_CHAT_ID, compliment)
        await asyncio.sleep(15)  # Отправляем комплимент каждые 15 секунд

# Обработчик кнопки "Достаточно, я довольна 😌"
@dp.message(lambda message: message.text == "Достаточно, я довольна 😌")
async def stop_carousel(message: types.Message):
    global carousel_active
    carousel_active = False
    await message.answer("Карусель комплиментов остановлена. 😊", reply_markup=keyboard)

# Обработчик кнопки "Внеплановый комплимент 💖"
@dp.message(lambda message: message.text == "Внеплановый комплимент 💖")
async def send_spontaneous_compliment(message: types.Message):
    compliment = random.choice(compliments)
    await bot.send_message(LENA_CHAT_ID, compliment)
    await message.answer("Внеплановый комплимент отправлен! 💖", reply_markup=keyboard)

# Запуск бота
async def main():
    await dp.start_polling(bot)

if __name__ == "__main__":
    asyncio.run(main())
